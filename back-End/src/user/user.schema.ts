import { EntitySchema } from 'typeorm';
import { Users } from './../Entities/Users.entity';

export const UserSchema = new EntitySchema<Users>({
  name: 'User',
  target: Users,
  columns: {
    id: {
      type: Number,
      primary: true,
      generated: true,
    },
    firstName: {
      type: String,
    },
    lastName: {
      type: String,
    },
    isActive: {
      type: Boolean,
      default: true,
    },
  },
  relations: {
    photos: {
      type: 'one-to-many',
      target: 'Photo', // the name of the PhotoSchema
    },
  },
});